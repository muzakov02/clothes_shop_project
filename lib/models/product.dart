class Product{
  final String imagePath;
  final String type;
  final String title;
  final String oldPrice;
  final String newPrice;
  final String discount;
  final String price;
  final String color;
  final String size;


  const Product({

    required this.imagePath,
    required this.type,
    required this.title,
    required this.oldPrice,
    required this.newPrice,
    required this.discount,
    required this.price,
    required this.color,
    required this.size,

  });

}